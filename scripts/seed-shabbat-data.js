async function seedShabbatData() {
    const baseURL = 'http://localhost:1337/api';
    const endpoint = '/shabbat-and-holidays';
    
    console.log('üïØÔ∏è  Starting Shabbat and Holidays data seeding...\n');
    
    let successCount = 0;
    let errorCount = 0;
    
    for (let i = 0; i < shabbatAndHolidays.length; i++) {
        const entry = shabbatAndHolidays[i];
        
        try {
            console.log(`üìÖ Creating entry ${i + 1}/20: ${entry.name}`);
            
            const response = await fetch(`${baseURL}${endpoint}`, {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                },
                body: JSON.stringify({
                    data: entry
                })
            });
            
            if (!response.ok) {
                const errorData = await response.json();
                throw new Error(`HTTP ${response.status}: ${JSON.stringify(errorData)}`);
            }
            
            const result = await response.json();
            console.log(`‚úÖ Successfully created: ${entry.name} (ID: ${result.data.id})`);
            successCount++;
            
        } catch (error) {
            console.log(`‚ùå Error creating ${entry.name}:`, error.message);
            errorCount++;
        }
        
        // Small delay to avoid overwhelming the server
        await new Promise(resolve => setTimeout(resolve, 100));
    }
    
    console.log('\nüéâ Seeding completed!');
    console.log(`‚úÖ Successfully created: ${successCount} entries`);
    console.log(`‚ùå Errors: ${errorCount} entries`);
    
    if (errorCount > 0) {
        console.log('\nüí° If you got errors, make sure:');
        console.log('   - Strapi is running on http://localhost:1337');
        console.log('   - The "Shabbat and Holidays" content type exists');
        console.log('   - The content type is accessible (check permissions)');
    }
}

// Execute the seeding
seedShabbatData().catch(console.error);